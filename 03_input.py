# The input() command pauses the program and waits for the user to type something.
# We store whatever they type into a variable!

print("--- The Greeting Robot ---")

# Asking the user for their name
user_name = input("What is your name? ")

# We can combine text and variables in print() by using a comma
print("It is very nice to meet you,", user_name)

# Let's ask another question!
