# Variables are like boxes that store data
name = "Ilsa Malik"  # This box stores text (a string)
favorite_number = 5  # This box stores a number (an integer)

# Let's print the boxes! Notice there are NO quotation marks around the variables.
print("My name is:")
print(name)

print("My favorite number is:")
print(favorite_number)

